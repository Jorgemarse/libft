#include "libft.h"

/*
	Show if it's a alpha or not.
*/

int	ft_isalpha(int c)
{
	if (!((c >= 'A' && c <= 'Z') \
		 || (c >= 'a' && c <= 'z')))
		return (0);
	return (1);
}

